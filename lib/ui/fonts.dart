import 'package:flutter/widgets.dart';

class Fonts {
  Fonts._();

  static TextStyle sourceCodePro({double? fontSize}) {
    return TextStyle(
      fontFamily: 'SourceCodePro',
      fontSize: fontSize,
    );
  }

  static TextStyle mPlusRounded1c({double? fontSize}) {
    return TextStyle(
      fontFamily: 'MPLUSRounded1c',
      fontSize: fontSize,
    );
  }
}
